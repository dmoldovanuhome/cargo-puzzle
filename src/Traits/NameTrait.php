<?php

namespace Emendis\Cargo\Traits;

trait NameTrait
{
    /** @var string  */
    protected string $name;

    /**
     * @return string
     */
    public function getName(): string
    {
        return $this->name;
    }

    /**
     * @param string $name
     */
    public function setName(string $name): void
    {
        $this->name = $name;
    }
}